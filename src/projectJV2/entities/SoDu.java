package projectJV2.entities;

public class SoDu {
    int tongthu;
    int tongchi;
    int sodu;

    public SoDu() {
    }

    public SoDu(int tongthu, int tongchi, int sodu) {
        this.tongthu = tongthu;
        this.tongchi = tongchi;
        this.sodu = sodu;
    }

    public int getTongthu() {
        return tongthu;
    }

    public void setTongthu(int tongthu) {
        this.tongthu = tongthu;
    }

    public int getTongchi() {
        return tongchi;
    }

    public void setTongchi(int tongchi) {
        this.tongchi = tongchi;
    }

    public int getSodu() {
        return sodu;
    }

    public void setSodu(int sodu) {
        this.sodu = sodu;
    }


}
